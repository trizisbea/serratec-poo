package exercicios;

public class TiposPrimitivos {
	public static void Casting() {
		int a = 1; 
		int b = 10;
		int soma = a + b; 
		
		System.out.println("A soma é " + soma);
		
		double divisao = ((double) a + (double) b) /2; 
		
		System.out.println("A divisao é " + divisao);
	}
}
